package com.upuphone.runasone.relay.lib.manager;

public class MetaBean {
    private String appUniteCode;
    private int mapCode;
    private String packageName;

    public String getAppUniteCode() {
        return this.appUniteCode;
    }

    public int getMapCode() {
        return this.mapCode;
    }

    public String getPackageName() {
        return this.packageName;
    }

    public void setAppUniteCode(String str) {
        this.appUniteCode = str;
    }

    public void setMapCode(int i) {
        this.mapCode = i;
    }

    public void setPackageName(String str) {
        this.packageName = str;
    }
}
